package com.stacktest.hospitalitaliano;

public class ResultadoDto {
	private String descriptionid;
	private String termino;
	private String prefDescid;
	private String preferido;

	public String getDescriptionid() {
		return descriptionid;
	}

	public void setDescriptionid(String descriptionid) {
		this.descriptionid = descriptionid;
	}

	public String getTermino() {
		return termino;
	}

	public void setTermino(String termino) {
		this.termino = termino;
	}

	public String getPrefDescid() {
		return prefDescid;
	}

	public void setPrefDescid(String prefDescid) {
		this.prefDescid = prefDescid;
	}

	public String getPreferido() {
		return preferido;
	}

	public void setPreferido(String preferido) {
		this.preferido = preferido;
	}
}
